package com.practicehome;

public class DataTypes {
    public static void main(String[] main) {
        boolean b = false;// default size 1 bit and default value false bit
        byte by = 1;// default size 1 byte and default value is 0
        short s = 2; // default size 2 byte and default value is 0
        char c = 'a'; // default size is 2 byte and value is 0
        int i = 3; // default size is 4 byte and value is 0
        float f = 0.1f; // default size is 4 byte and value is 0.0f
        long l = 4l; // default size is 8 byte and value is 0l
        double d = 5; // default size is 8 and value is 0.0d
    /*There are two data types in java 1st is primitive and 2nd is non primitive . above 8 data types are
    primitive data types with default and and default value
    and non primitive data types are String, Array ,classes and interfaces.
    * */


    }
}
