package com.hundredjavaprogram;

public class multiplicationTable {
    public static void main(String[] args) {
        int n = 2;
        for (int i=1;i<=10;i++){
            System.out.println(n*i);
    }}}
    class ReverseTable{
        public static void main(String[] args) {
          int n =4;
          for (int i =10;i>=1;i--){
              System.out.println(n*i);
          }
        }

    }

